function calculate(arr) {
    let max = arr [0]
    let min = arr [0]
    let sum = 0

    for ( let i = 0; i < arr.length; i++) {
        if (arr[i] > max) {
            max = arr[i]
        } else if(arr[i] < min){
            min = arr[i]
        }
        sum = sum + arr[i]
    }

    const obj = {
        min :min,
        max: max,
        avg: sum / arr.length
    }
   
    return obj

    
}
